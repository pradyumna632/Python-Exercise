''' Data Types in python'''

from typing import Tuple


a = 10  # int  
b = 20.0 # Float
c = 3+1j # Complex (x+yj : where x is 'REAL' part and y is imaginary part)
d  = "String DataType"
list_DataType = [1,2,3,4,5]
Tuple_DataType = (1,2,3,4,5)
set_DataType = {1,2,3,4,4}
Dictionary_DataType = {1: 'One' , 2: 'Two' , 'Three' : 3}

print(" {}\n {}\n {}\n {}\n {}\n {}\n {}\n {}\n ".format(a,b,c,d,list_DataType,Tuple_DataType,
                                                            set_DataType,Dictionary_DataType))
